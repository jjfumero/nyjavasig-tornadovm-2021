if [ -z "${TORNADO_SDK}" ]; then
  echo "Please ensure the TORNADO_SDK environment variable is set correctly"
  exit 0
fi

if [ -z "${JAVA_HOME}" ]; then
  echo "Please ensure the JAVA_HOME environment variable is set correctly"
  exit 0
fi

function print_usage() {
  echo "usage:    tornado [options] class [arg0 ... argN]"
  echo ""
  echo "              -h|--help                       Show this message"
  echo "              -d|--debug                      Enable debug output"
  echo ""
  echo "          Debugging options:"
  echo ""
  echo "              --debug                         Print debugging information"
  echo "              --printBytecodes                Print TornadoVM bytecodes"
  echo "              --igv                           Dump GRAAL IR into IGV"
  echo "              --igvLowTier                    Dump OpenCL Low Tier with IGV"
  echo "              --printKernel                   Print autogenerated OpenCL C Kernel"
  echo "              --ea|--enableassertions         Enable Java assertions"
  echo ""
  echo "          TornadoVM settings:"
  echo ""
  echo "              --printFlags                    Print TornadoVM Java flags and exit"
  echo "              --version                       Print TornadoVM version information and exit"
  echo "              --devices                       Print TornadoVM available devices and exit"
  echo ""
  echo "          TornadoVM display all options:"
  echo ""
  echo "              --options                       Print all TornadoVM options and flags, and exit"
  echo ""
  echo "          Maven:"
  echo ""
  echo "              --mavenLocalInstall             Install all maven packages locally"
  echo ""
  exit 0
}

function print_version() {
  cat "${TORNADO_SDK}/etc/tornado.release"
  exit 0
}

function print_all_options() {
  export COLOR_CYAN='\e[0;36m'
  export COLOR_NC='\e[0m' # No Color
  echo "TornadoVM options"
  echo ""
  echo -e "$COLOR_CYAN Debugging Information: $COLOR_NC"
  echo ""
  echo -e "\tPrint all devices available   : --devices "
  echo -e "\tPrint generated kernel        : -Dtornado.print.kernel=True "
  echo -e "\tPrint kernel debug information: -Dtornado.debug=True "
  echo -e "\tEnable full debug information : -Dtornado.fullDebug=True "
  echo -e "\tDump compiled graphs into IGV : -Dgraal.Dump=*:5 -Dgraal.PrintGraph=Network -Dgraal.PrintCFG=false "
  echo ""
  echo -e "$COLOR_CYAN Define on which device the application should run (by default is 0): $COLOR_NC"
  echo ""
  echo -e "\tChange the driver and device to run the application: -D<taskScheduleName>.<taskName>=<driverID>:<deviceID> (e.g., -Ds0.t0.device=1:2)"
  echo -e "\tChange the driver order: -Dtornado.{ptx,opencl}.priority=X (e.g., -Dtornado.opencl.priority=2)"
  echo ""
  echo -e "$COLOR_CYAN Profiler: $COLOR_NC"
  echo ""
  echo -e "\tEnable online profiler        : -Dtornado.profiler=True "
  echo -e "\tSave profiler information     : -Dtornado.profiler=True -Dtornado.profiler.dump.dir=FILENAME.json"
  echo -e "\tEnable profiler and print at the end of the execution: -Dtornado.profiler=True -Dtornado.log.profiler=True "
  echo ""
  echo -e "$COLOR_CYAN Device Heap: $COLOR_NC"
  echo ""
  echo -e "\tResize the device heap (1GB by default): -Dtornado.heap.allocation=512MB"
  echo ""
  echo -e "$COLOR_CYAN FPGA: $COLOR_NC"
  echo ""
  echo -e "\tLoad the FPGA configuration file: -Dtornado.fpga.conf.file=<file>"
  echo -e "\tGet a precompiled binary        : -Dtornado.precompiled.binary=<path> "
  echo ""
  echo -e "$COLOR_CYAN Optimizations: $COLOR_NC"
  echo ""
  echo -e "\tApply full inlining             : -Dtornado.compiler.fullInlining=True  (False by default)"
  echo ""
}

JAVA_CMD=${JAVA_HOME}/bin/java
JAVA_VERSION_OUTPUT=$("$JAVA_CMD" -version 2>&1)
JAVA_VERSION=$(echo "$JAVA_VERSION_OUTPUT" | awk -F[\"\.] -v OFS=. 'NR==1{print $2,$3}')

if [[ $JAVA_VERSION == "9.0" || $JAVA_VERSION == "10.0" ]]; then
  echo "TornadoVM does not support Java 9 and 10"
  exit 0
fi

case "$(uname -s)" in
   CYGWIN*|MINGW32*|MSYS*|MINGW*)
     PATH_SEPARATOR=";"
     ;;

   *)
     PATH_SEPARATOR=":"
     ;;
esac
EXTERNAL_DIRS_P=""
if test "${EXTERNAL_DIRS+defined}"; then
  EXTERNAL_DIRS_P="${PATH_SEPARATOR}${EXTERNAL_DIRS}"
fi 
TORNADO_FLAGS="--vm.Djava.library.path=${TORNADO_SDK}/lib${EXTERNAL_DIRS_P}"
if [ "$JAVA_VERSION" = "1.8" ]; then
  TORNADO_FLAGS="${TORNADO_FLAGS} --vm.Djava.ext.dirs=${TORNADO_SDK}/share/java/tornado"
else
  TORNADO_FLAGS="${TORNADO_FLAGS} --module-path .:${TORNADO_SDK}/share/java/tornado"

  # If extra values for module path have been specified by the user, we must append them
  if [[ $@ == *"--module-path"* ]]; then
    USER_MODULE_PATH=$(echo $@ | sed 's/.*--module-path //' | sed 's/ .*//')

    # Remove the user module path entry from the positional parameters
    for arg in "$@"; do
      shift
      case $arg in
        (--module-path) shift ;;
        (*) set -- "$@" "$arg" ;;
      esac
    done

    TORNADO_FLAGS="${TORNADO_FLAGS}:${USER_MODULE_PATH}"
  fi
fi

printflags=0
printdevices=0
enableAssertions=0

POSITIONAL=()
while [[ $# -gt 0 ]]; do
  key="$1"

  case $key in
  --version)
    print_version
    shift
    ;;
  -d | --debug)
    TORNADO_FLAGS="${TORNADO_FLAGS} --vm.Dtornado.debug=True"
    shift # past argument
    ;;
  --igv)
    TORNADO_FLAGS="${TORNADO_FLAGS} --vm.Dgraal.Dump=*:5 --vm.Dgraal.PrintGraph=Network --vm.Dgraal.PrintCFG=false"
    shift # past argument
    ;;
  --igvLowTier)
    TORNADO_FLAGS="${TORNADO_FLAGS} -Dgraal.Dump=*:1 -Dgraal.PrintGraph=Network -Dgraal.PrintCFG=true -Dtornado.debug.lowtier=True "
    shift
    ;;
  --printKernel)
    TORNADO_FLAGS="${TORNADO_FLAGS} --vm.Dtornado.print.kernel=True "
    shift
    ;;
  --printBytecodes)
    TORNADO_FLAGS="${TORNADO_FLAGS} --vm.Dtornado.print.bytecodes=True "
    shift
    ;;
  --mavenLocalInstall)
    tornadoLocalInstallMaven
    exit 0
    ;;
  --printFlags)
    printflags=1
    shift
    ;;
  --devices)
    printdevices=1
    shift
    ;;
  --options)
    print_all_options
    exit 0
    ;;
  -h | --help)
    print_usage
    shift
    ;;
  -ea | --ea | -enableassertions | --enableassertions)
    enableAssertions=1
    shift
    ;;
  *) # unknown option
    POSITIONAL+=("$1") # save it in an array for later
    shift              # past argument
    ;;
  esac
done
set -- "${POSITIONAL[@]}" # restore positional parameters

PROVIDERS=" \
--vm.Dtornado.load.api.implementation=uk.ac.manchester.tornado.runtime.tasks.TornadoTaskSchedule \
--vm.Dtornado.load.runtime.implementation=uk.ac.manchester.tornado.runtime.TornadoCoreRuntime \
--vm.Dtornado.load.tornado.implementation=uk.ac.manchester.tornado.runtime.common.Tornado \
--vm.Dtornado.load.device.implementation.opencl=uk.ac.manchester.tornado.drivers.opencl.runtime.OCLDeviceFactory \
--vm.Dtornado.load.device.implementation.ptx=uk.ac.manchester.tornado.drivers.ptx.runtime.PTXDeviceFactory \
--vm.Dtornado.load.annotation.implementation=uk.ac.manchester.tornado.annotation.ASMClassVisitor \
--vm.Dtornado.load.annotation.parallel=uk.ac.manchester.tornado.api.annotations.Parallel "

JAVA_FLAGS=""
if [ $enableAssertions -eq 1 ]; then
  # We do not satisfy the Graal compiler assertions because we only support a subset of the Java specification.
  # This allows us have the GraalIR in states which normally would be illegal.
  JAVA_FLAGS="-ea -da:org.graalvm.compiler... "
fi

JAVA_FLAGS="--jvm --polyglot --vm.XX:-UseCompressedOops ${TORNADO_FLAGS} ${PROVIDERS} "
if [ "$JAVA_VERSION" = "1.8" ]; then
  JAVA_FLAGS="${JAVA_FLAGS} --vm.XX:-UseJVMCIClassLoader "
else
  # We have a Java 11+ version
  COMMON_EXPORTS="${TORNADO_SDK}/etc/exportLists/common-exports"
  OPENCL_EXPORTS="${TORNADO_SDK}/etc/exportLists/opencl-exports"
  PTX_EXPORTS="${TORNADO_SDK}/etc/exportLists/ptx-exports"
  UPGRADE_MODULE_PATH="--upgrade-module-path ${TORNADO_SDK}/share/java/graalJars"

  TORNADO_ADD_MODULES="--add-modules ALL-SYSTEM,tornado.runtime,tornado.annotation,tornado.drivers.common"
  PTX_MODULE="tornado.drivers.ptx"
  OPENCL_MODULE="tornado.drivers.opencl"

  JAVA_FLAGS="${JAVA_FLAGS} -XX:+UseParallelOldGC -XX:-UseBiasedLocking @${COMMON_EXPORTS} "
  # If the current Java build is not a GraalVM build, then we need to patch the module path for the compiler
  if [[ ! $JAVA_VERSION_OUTPUT == *"GraalVM"* ]]; then
    JAVA_FLAGS="${JAVA_FLAGS} ${UPGRADE_MODULE_PATH} "
  fi
  read -ra selected_backends < "${TORNADO_SDK}/etc/tornado.backend"
  if [[ ${selected_backends} == *"ptx-backend"* ]]; then
    JAVA_FLAGS="${JAVA_FLAGS} @${PTX_EXPORTS}"
    TORNADO_ADD_MODULES="${TORNADO_ADD_MODULES},${PTX_MODULE}"
  fi
  if [[ ${selected_backends} == *"opencl-backend"* ]]; then
    JAVA_FLAGS="${JAVA_FLAGS} @${OPENCL_EXPORTS}"
    TORNADO_ADD_MODULES="${TORNADO_ADD_MODULES},${OPENCL_MODULE}"
  fi
  JAVA_FLAGS="${JAVA_FLAGS} ${TORNADO_ADD_MODULES}"
fi

if [ $printflags -eq 1 ]; then
  echo "$JAVA_CMD $JAVA_FLAGS"
  exit 0
fi

if [ $printdevices -eq 1 ]; then
  tornado uk.ac.manchester.tornado.drivers.TornadoDeviceQuery verbose
  exit 0
fi

JAVA_FLAGS="--jvm --polyglot --vm.XX:-UseCompressedOops ${TORNADO_FLAGS} ${PROVIDERS} "
if [ "$JAVA_VERSION" = "1.8" ]; then
  JAVA_FLAGS="${JAVA_FLAGS} --vm.XX:-UseJVMCIClassLoader "
elif [ "$JAVA_VERSION" = "11.0" ]; then
  JAVA_FLAGS="${JAVA_FLAGS} --vm.XX:+UseParallelOldGC --vm.XX:-UseBiasedLocking ${PACKAGE_LIST[*]} "
fi

# shellcheck disable=SC2068
PYTHON=${JAVA_HOME}/bin/graalpython

echo $PYTHON
echo $JAVA_FLAGS

${PYTHON} ${JAVA_FLAGS} $@
